using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SECI.Entities
{
    public class Resultado
    {
        public int ProcesoExitoso { get; set; }

        public string Mensaje { get; set; }

        public int Habilitar { get; set; }

        public int IdEmpresa { get; set; }

        public int Numero { get; set; }

        public string Cadena { get; set; }

        public string Nombre { get; set; }

    }

    public class Resultado<T>
    {
        public Resultado()
        {
        }

        public List<T> ListadoBusqueda { get; set; }

        public int ProcesoExitoso { get; set; }

        public string Mensaje { get; set; }

        public List<T> Lista { get; set; }

        public List<T> Listado { get; set; }

        public int Habilitar { get; set; }

        public int HabilitarEliminacion { get; set; }

        public int IdEmpresa { get; set; }

        public T Objeto { get; set; }

        public int Numero { get; set; }

        public string Cadena { get; set; }

        public string CadenaInicio { get; set; }

        public int IdUsuario { get; set; }

        public int IdTipoUsuario { get; set; }

        public int IdPerfil { get; set; }
    }

    public class Resultado<T, E>
    {
        public Resultado()
        {
        }

        public int ProcesoExitoso { get; set; }

        public string Mensaje { get; set; }

        public List<T> Lista { get; set; }

        public List<T> Listado { get; set; }

        public int Habilitar { get; set; }

        public int HabilitarEliminacion { get; set; }

        public int IdEmpresa { get; set; }

        public E Objeto { get; set; }

        public int Numero { get; set; }

        public string Cadena { get; set; }

        public string CadenaInicio { get; set; }
    }

}
